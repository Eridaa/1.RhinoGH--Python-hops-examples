def addition(num1, num2):
    sum = num1 + num2
    return sum


def substraction(num1, num2):
    sub = num1 - num2
    return sub


def multiplication(num1, num2):
    mult = num1 * num2
    return mult


def division(num1, num2):
    if num2 != 0: 
        div = num1 / num2
        return div
    else:
        return None

